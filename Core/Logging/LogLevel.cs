namespace MacroEngine.Core.Logging
{
    /// <summary>
    /// Niveaux de log disponibles
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// Messages de débogage détaillés (développement uniquement)
        /// </summary>
        Debug,

        /// <summary>
        /// Informations générales sur l'exécution
        /// </summary>
        Info,

        /// <summary>
        /// Avertissements (problèmes non bloquants)
        /// </summary>
        Warning,

        /// <summary>
        /// Erreurs nécessitant une attention
        /// </summary>
        Error
    }
}






